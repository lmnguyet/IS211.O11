    CustomerID VARCHAR2(5) NOT NULL,
    CumulativeTotal NUMBER,
    CONSTRAINT PK_CUSTOMERMANAGER PRIMARY KEY (CustomerID)
);

-- Setting table CN2.ITEM (Replication) 
CREATE TABLE CN2.ITEM (
    ItemID VARCHAR2(5) NOT NULL,
    ItemName VARCHAR2(50) NOT NULL,
    ItemType VARCHAR2(50) NULL,
    SalePrice NUMBER NULL,
    CONSTRAINT PK_ITEM PRIMARY KEY (ItemID)
);

-- Setting table CN2.ITEMMANAGE_MANAGER (Phan manh doc khong du thua tu bang MANAGEITEM) 
CREATE TABLE CN2.ITEMMANAGE_MANAGER (
    BranchID VARCHAR2(5) NOT NULL,
    ItemID VARCHAR2(5) NOT NULL,
    SaleStatus VARCHAR2(25) NULL,
    Discount NUMBER NULL,
    CONSTRAINT PK_ITEMMANAGER PRIMARY KEY (BranchID, ItemID)
);

-- Setting table CN2.ITEMMANAGE_STAFF (Phan manh doc khong du thua tu bang MANAGEITEM) 
CREATE TABLE CN2.ITEMMANAGE_STAFF (
    BranchID VARCHAR2(5) NOT NULL,
    ItemID VARCHAR2(5) NOT NULL,
    ServingTime VARCHAR2(25) NULL,
    Availability NUMBER NULL,
    CONSTRAINT PK_ITEMSTAFF PRIMARY KEY (BranchID, ItemID)
);

-- Setting table CN2.BILL (Phan manh ngang dan xuat)
CREATE TABLE CN2.BILL (
    BillID VARCHAR2(5) NOT NULL,
    BillDate DATE NOT NULL,
    CustomerID VARCHAR2(5) NOT NULL,
    EmployeeID VARCHAR2(5) NOT NULL,
    Total NUMBER NULL,
    CONSTRAINT PK_BILL PRIMARY KEY (BillID)
);

-- Setting table CN2.BILLDETAIL 
CREATE TABLE CN2.BILLDETAIL (
    BillID VARCHAR2(5) NOT NULL,
    ItemID VARCHAR2(5) NOT NULL,
    Quantity NUMBER NULL,
    SubTotal NUMBER NULL,
    CONSTRAINT PK_BILLDETAIL PRIMARY KEY (BillID, ItemID)
);
