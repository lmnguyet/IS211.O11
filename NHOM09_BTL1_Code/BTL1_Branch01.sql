    CustomerID VARCHAR2(5) NOT NULL,
    CustomerName VARCHAR2(50) NOT NULL,
    PhoneNumber VARCHAR2(10) NULL,
    CustomerAddress VARCHAR2(50) NULL,
    Birthday DATE NULL,
