    EmployeeID VARCHAR2(5) NOT NULL,
    EmployeeName VARCHAR2(50) NOT NULL,
    Birthday DATE NULL,
    PhoneNumber VARCHAR2(10) NULL,
    EmployeeAddress VARCHAR2(50) NULL,
